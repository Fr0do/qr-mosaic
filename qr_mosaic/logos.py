def get_logo_svg(preset_name: str) -> str:
    path_data = LOGO_PRESETS.get(preset_name)
    if path_data is None:
        raise ValueError(
            f"Unknown logo preset '{preset_name}'. "
            f"Available: {', '.join(LOGO_PRESETS)}"
        )
    return (
        '<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24">'
        f'<path d="{path_data}" fill="currentColor" stroke="none"/>'
        "</svg>"
    )
